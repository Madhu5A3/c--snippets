#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    // Math

    // max function to find the maximum of two numbers
    cout << max(5, 10) << endl;

    // min function to find the minimum of two numbers
    cout << min(5, 10) << endl;

    // sqrt,round,log --> cmath
    cout << sqrt(64) << endl;
    cout << round(6.4) << endl;
    cout << log(64) << endl;
    cout << pow(2, 3) << endl;
    return 0;
}