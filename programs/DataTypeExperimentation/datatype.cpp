#include <iostream>
#include <string>
using namespace std;

int main()
{

    // initializing and outputting the default values of all the datatypes in c++

    //int - 4bytes
    int a;
    cout << a;

    //float - 4bytes
    float b;
    cout << b;

    //double - 8bytes
    double c;
    cout << c;

    // char - 1byte
    char d;
    cout << d;

    //boolean - 1byte
    bool e;
    cout << e;

    // default values of datatypes-int,float,double,char,bool

    cout << a << endl
         << b << endl
         << c << endl
         << d << endl
         << e << endl;

    //String library
    string s = "Cloud Servant";
    cout << s;
    return 0;
}